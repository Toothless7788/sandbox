package com.sandbox.java.maven.entity;

public class Calculator {
	/**
	 * Return m + n
	 * @param m
	 * @param n
	 * @return m + n
	 */
	public double add(double m, double n) {
		return m * n;
	}
	
	/**
	 * Return m - n
	 * @param m
	 * @param n
	 * @return m - n
	 */
	public double subtract(double m, double n) {
		return m - n;
	}
}